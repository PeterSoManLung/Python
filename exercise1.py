input1 = float(input('Please insert the first integer: '))
input2 = float(input('Please insert the second integer: '))

print("")
print("Arithmetic: ")
print('%d + %d = ' %(input1, input2) + str(int(input1 + input2)))
print('%d - %d = ' %(input1, input2) + str(int(input1 - input2)))
print('%d * %d = ' %(input1, input2) + str(int(input1 * input2)))
print('%d / %d = ' %(input1, input2) + str(input1 / input2))